                <h2>Teknik Pembuatan Jurnal/Artikel</h2>
              </div>
              <div class="article-content" style="text-align: justify;">
                <p> Jurnal merupakan sebuah publikasi periodik dalam bentuk artikel yang diterbitkan secara berkala, dalam hal ini biasanya jurnal diterbitkan pada interval waktu tertentu seperti setiap 4 bulan atau setiap 1 tahun. Jurnal memiliki beberapa jenis yang diantaranya adalah Professional or Trade Journals, Popular Journals, dan Scholarly Journals.</p>
                <p> Pada umumnya jurnal memiliki cakupan materi yang luas namun sangat padat, hanya terdiri dari 8 hingga 12 halaman, namun di setiap kalimatnya bernilai ilmu pengetahuan. Tujuan pembuatan jurnal adalah untuk mengembangkan sebuah penelitian yang telah dituliskan serta menjadi acuan untuk para peneliti lainnya sedang melakukan kegiatan penelitian yang sejenis. </p>               
                <p> Jurnal pada umumnya berisi sejumlah referensi yang menjadi rujukan penulisan tiap artikel. Jenis artikel yang ditulis tak sebatas laporan penelitian, namun bisa pula berupa review literatur. Adapun susunan jurnal/artikel yang benar yaitu: </p>
                <p> <strong>1. Judul</strong><br>Setiap jurnal ilmiah harus memiliki judul yang jelas. Dengan membaca judul, akan memudahkan pembaca mengetahui inti jurnal tanpa harus membaca keseluruhan dari jurnal tersebut. Judul tidak boleh memiliki makna ganda. Disarankan tidak boleh lebih dari 12 kata jurnal berbahasa Indonesia dan lebih dari 10 kata jurnal berbahasa Inggris. </p>
                <p> <strong>2. Identitas Penulis</strong><br>Penulisan nama Penulis tanpa gelar akademik dianjurkan disertai nama lembaga (afiliasi: nama prodi, fakultas, dan universitas), serta dianjurkan menyertakan alamat dan email. </p>
                <p> <strong>3. Abstrak</strong><br>Abstrak berbeda dengan ringkasan. Bagian abstrak dalam jurnal ilmiah berfungsi untuk mencerna secara singkat isi jurnal. Abstrak di sini dimaksudkan utnuk menjadi penjelas tanpa mengacu pada jurnal. Bagian abstrak harus menyajikan sekitar 150-250 kata yang merangkum tujuan, metode, hasil dan kesimpulan. Jangan gunakan singkatan atau kutipan dalam abstrak. Pada abstrak harus berdiri sendiri tanpa catatan kaki. Abstrak ini biasanya ditulis terakhir. Cara mudah untuk menulis abstrak adalah mengutip poin-poin paling penting di setiap bagian jurnal. Kemudian menggunakan poin-poin untuk menyususn deskripsi singkat tentang jurnal yang telah dibuat. Penulisan abstrak diketik menggunakan 1 spasi. Kata kunci sebanyak 3-5 kata, diambil dari inti yang akan dibahas dalam penelitian. </p>
                <p> <strong>4. Pendahuluan</strong><br>Pendahuluan berisi latar belakang mengapa penelitian dilakukan, uraian permasalahan yang akan diteliti, dikaitkan dengan teori, dan diakhiri dengan tujuan dilaksanakan penelitian tersebut. Penulisan diketik dengan 1.5 spasi, kurang lebih 4-6 halaman. </p>
                <p> <strong>5. Metode Penelitian</strong><br>Bagian ini menjelaskan ketika percobaan telah dilakukan. Peneliti menjelaskan desain percobaan, peralatan, metode pengumpulan data, dan teknik analisis data. Jika percobaan dilakukan di alam, maka penulis menggambarkan daerah penelitian, lokasi, dan juga menjelaskan pekerjaan yang dilakukan. Aturan umum yang perlu diingat adalah bagian ini harus memaparkan secara rinci dan jelas sehingga pembaca memiliki pengantahuan dan teknik dasar agar bisa dipublikasikan. Penulisan Metode diketik dengan 1.5 spasi, kurang lebih 1 halaman. </p>
                <p> <strong>6. Pembahasan/Hasil Pembahasan</strong><br>Pembahasan dapat dibagi dalam beberapa sub bagian. Diketik dalam 1.5 spasi. Penulisan kurang lebih 4-6 halaman. Dalam pembahasan membandingkan hasil penelitian dengan model atau teori yang diacu, dan menghubungkan hasil penelitian Anda dan penelitian sebelumnya dengan menunjukkan persamaan dan membahas perbedaannya. Pembahasan digunakan untuk hasil penelitian kualitatif, sedangkan Hasil dan Pembahasan digunakan untuk hasil penelitian kuantitatif. </p>
                <p> <strong>7. Simpulan</strong><br>Dalam simpulan yang dibahas pembuktian hipotesis dari penelitian, ditulis ringkas yang memuat informasi yang cukup sehingga pembaca mengetahui bahwa telah membuktikan hipotesis yang telah dilakukan dan dalam mengetahui kelebihan dan kekurangan metode. Dan biasanya terdapat saran yang berisi kemungkinan penelitian lebih lanjut, dan potensi-potensi yang dimiliki metode yang dipakai dapat dimasukkan. </p>
                <p> <strong>8. Daftar Pustaka</strong><br>Daftar pustaka pada karya ilmiah ditulis langsung setelah teks berakhir (tidak perlu ganti halaman baru), sedangkan daftar pustaka pada makalah, buku, atau penelitian ditulis dengan berganti halaman baru. Jenis penulisan daftar pustaka diberi judul DAFTAR PUSTAKA, dicetak tebal dengan huruf tegak, kapital semua.<br><br> Unsur yang ditulis dalam daftar pustaka secara berturut-turut meliputi: (1) nama pengarang ditulis dengan urutan: nama akhir, nama awal, dan nama tengah, tanpa gelar akademik, (2) tahun penerbitan, (3) judul, termasuk subjudul, (4) tempat penerbitan, dan (5) nama penerbit. Unsure-unsur tersebut dapat bervariasi bergantung kepada jenis sumber pustakanya. </p>
